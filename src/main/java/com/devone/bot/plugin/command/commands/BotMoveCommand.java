package com.devone.bot.plugin.command.commands;

import java.util.Arrays;

import org.bukkit.Location;
import org.bukkit.command.Command;
import org.bukkit.command.CommandExecutor;
import org.bukkit.command.CommandSender;

import com.devone.bot.core.Bot;
import com.devone.bot.core.BotManager;
import com.devone.bot.core.task.passive.BotTaskManager;
import com.devone.bot.core.task.active.move.BotMoveTask;
import com.devone.bot.core.task.active.move.params.BotMoveTaskParams;
import com.devone.bot.core.task.reactive.container.BotReactiveEmptyContainer;
import com.devone.bot.core.utils.blocks.BotPosition;
import com.devone.bot.core.utils.logger.BotLogger;

public class BotMoveCommand implements CommandExecutor {

    private final BotManager botManager;

    public BotMoveCommand(BotManager botManager) {
        this.botManager = botManager;
    }

    @Override
    public boolean onCommand(CommandSender sender, Command command, String label, String[] args) {

        BotLogger.debug("üîß ", true, "–ü–æ–ª—É—á–µ–Ω–∞ –∫–æ–º–∞–Ω–¥–∞ –æ—Ç —Å–µ—Ä–≤–µ—Ä–∞: " + Arrays.toString(args));

        if (args.length < 4) {
            sender.sendMessage("‚ùå –ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤. –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ: /bot-move <bot_id> <x> <y> <z>");
            BotLogger.debug("‚ùå", true, "–ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ –¥–ª—è /bot-move");
            return false;
        }

        String botName = args[0];

        int x, y, z;

        try {
            x = Integer.parseInt(args[1]);
            y = Integer.parseInt(args[2]);
            z = Integer.parseInt(args[3]);

        } catch (NumberFormatException e) {

            sender.sendMessage("‚ùå –ö–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã –¥–æ–ª–∂–Ω—ã –±—ã—Ç—å —Ü–µ–ª—ã–º–∏ —á–∏—Å–ª–∞–º–∏.");

            BotLogger.debug("‚ùå ", true, "–ö–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã –¥–æ–ª–∂–Ω—ã –±—ã—Ç—å —Ü–µ–ª—ã–º–∏ —á–∏—Å–ª–∞–º–∏.");

            return false;
        }

        Bot bot = botManager.getBot(botName);

        if (bot == null) {
            sender.sendMessage("‚ùå –ë–æ—Ç —Å –∏–º–µ–Ω–µ–º " + botName + " –Ω–µ –Ω–∞–π–¥–µ–Ω.");

            BotLogger.debug("‚ùå", true, "–ë–æ—Ç —Å –∏–º–µ–Ω–µ–º " + botName + " –Ω–µ –Ω–∞–π–¥–µ–Ω.");

            return false;
        }

        // BotTaskManager.clear(bot);
        
        // ‚úÖ –î–æ–±–∞–≤–ª—è–µ–º –∑–∞–¥–∞—á—É –Ω–∞ –ø–µ—Ä–µ–º–µ—â–µ–Ω–∏–µ
        Location targetLocation = new Location(bot.getNPCEntity().getWorld(), x, y, z);
        // —Å–æ–∑–¥–∞–µ–º –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä
        BotReactiveEmptyContainer container = new BotReactiveEmptyContainer(bot);
        BotMoveTask moveTask = new BotMoveTask(bot);
        BotMoveTaskParams moveTaskParams = new BotMoveTaskParams();
        moveTaskParams.setTarget(new BotPosition(x, y, z));
        moveTask.setParams(moveTaskParams);
        container.add(moveTask);
        BotTaskManager.push(bot, container);

        BotLogger.debug("üìå ", true, "/bot-move: –ë–æ—Ç " + bot.getId() + " –Ω–∞–ø—Ä–∞–≤–ª—è–µ—Ç—Å—è –≤ " + targetLocation);
        
        sender.sendMessage("‚úÖ –ë–æ—Ç '" + botName + "' –Ω–∞–ø—Ä–∞–≤–ª—è–µ—Ç—Å—è –≤ " + x + " " + y + " " + z);

        return true;

    }

}
